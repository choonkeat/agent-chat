func builtinEmojiComplete(query string) ([]autocompleteItem, bool) {
	query = strings.ToLower(strings.TrimSpace(query))

	if query == "" {
		items := make([]autocompleteItem, len(popularEmojis))
		for i, e := range popularEmojis {
			items[i] = autocompleteItem{V: e.emoji, H: e.keywords[0]}
		}
		return items, true
	}

	type scored struct {
		emoji   string
		keyword string
		score   int
	}

	var results []scored

	for _, entry := range emojiList {
		bestScore := -1
		bestKW := entry.keywords[0]
		for _, kw := range entry.keywords {
			kwLower := strings.ToLower(kw)
			if kwLower == query {
				if bestScore < 0 || 0 < bestScore {
					bestScore = 0
					bestKW = kw
				}
			} else if strings.HasPrefix(kwLower, query) {
				s := 1
				if bestScore < 0 || s < bestScore {
					bestScore = s
					bestKW = kw
				}
			} else if strings.Contains(kwLower, query) {
				s := 2
				if bestScore < 0 || s < bestScore {
					bestScore = s
					bestKW = kw
				}
			} else if bestScore < 0 {
				// fuzzy: check if all query chars appear in order
				qi := 0
				first := -1
				last := -1
				for ki := 0; ki < len(kwLower) && qi < len(query); ki++ {
					if kwLower[ki] == query[qi] {
						if first < 0 {
							first = ki
						}
						last = ki
						qi++
					}
				}
				if qi == len(query) {
					span := last - first
					s := 3 + span
					if bestScore < 0 || s < bestScore {
						bestScore = s
						bestKW = kw
					}
				}
			}
		}
		if bestScore >= 0 {
			results = append(results, scored{entry.emoji, bestKW, bestScore})
		}
	}

	sort.SliceStable(results, func(i, j int) bool {
		return results[i].score < results[j].score
	})

	limit := 50
	hasMore := len(results) > limit
	if hasMore {
		results = results[:limit]
	}

	items := make([]autocompleteItem, len(results))
	for i, r := range results {
		items[i] = autocompleteItem{V: r.emoji, H: r.keyword}
	}
	return items, hasMore
}
